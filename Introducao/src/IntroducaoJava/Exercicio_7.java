package IntroducaoJava;

import java.util.Scanner;

public class Exercicio_7 {

	public static void main(String[] args) {
		
		Scanner in= new Scanner (System.in);
		float a=0, b=0, c=0, d=0, e=0, f=0, x=0, y=0;
		
		System.out.print("Insira o valor de A: ");
		a = in.nextFloat();
		System.out.print("Insira o valor de B: ");
		b = in.nextFloat();
		System.out.print("Insira o valor de C: ");
		c = in.nextFloat();
		System.out.print("Insira o valor de D: ");
		d = in.nextFloat();
		System.out.print("Insira o valor de E: ");
		e = in.nextFloat();
		System.out.print("Insira o valor de F: ");
		f = in.nextFloat();

		x=(c*e-b*f)/(a*e-b*d);
		y=(a*f-c*d)/(a*e-b*d);
		
		System.out.println("Conforme os valores informados o valor de X é "+x+" e o valor de Y é "+y);		

	}

}
